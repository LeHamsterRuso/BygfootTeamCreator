public class futbolista
{
	public String nombre;
	public int año, mes, skill, talento, rol;
	public futbolista(String nombre, int año, int mes, int skill, int talento, int rol)
	{
		this.nombre = nombre;
		this.año = año;
		this.mes = mes;
		this.skill = skill;
		this.talento = talento;
		this.rol = rol;
	}
}
